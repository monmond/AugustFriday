import UIKit



//MARK: - Constraint
public extension CGFloat {
  
  public static var zero: CGFloat {
    return .leastNormalMagnitude
  }
  
}



//MARK: - Convert
public extension CGFloat {
  
  public var toFloat: Float {
    return Float(self)
  }
  
}




